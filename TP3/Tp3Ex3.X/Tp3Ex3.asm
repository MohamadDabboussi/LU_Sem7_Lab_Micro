LIST p=16F887
#include <p16F887.inc>
__CONFIG _CONFIG1, _WDT_OFF & _HS_OSC
ORG 0x000
 GOTO start
ORG 0x05
start
BSF STATUS,RP0
BSF STATUS,RP1
CLRF ANSELH
CLRF ANSEL
BSF STATUS,RP0
BCF STATUS,RP1
CLRF TRISD
MOVLW B'00000100'
MOVWF OPTION_REG
BCF STATUS,RP0
BCF STATUS,RP1
LOOP
CLRF PORTD
CALL DELAY
MOVLW 0XFF
MOVWF PORTD
CALL DELAY
GOTO LOOP
DELAY
MOVLW .250
MOVWF 0X020
WAIT1
MOVLW 0X06
 MOVWF TMR0
WAIT
BTFSS INTCON,T0IF
GOTO WAIT
BCF INTCON,T0IF
DECFSZ 0x020,F
GOTO WAIT1
RETURN
END 


