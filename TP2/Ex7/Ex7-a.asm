STATEPT DS.W 1 ; STATE POINTER
 ORG $1000 ; MAIN PROGRAM
 MOVEA.L #S1,A0
 MOVE.W A0,STATEPT
FSM
 MOVEA.W STATEPT,A0
 MOVE.B OUT(A0),$E003
 MOVE.B WAIT(A0),D0
 BSR DELAY
 MOVE.B $E001,D1
 ANDI.W #$0F,D1
 LSL.W #$01,D1
 ADD.W D1,A0
