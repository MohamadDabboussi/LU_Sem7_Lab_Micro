 BRA FSM
DELAY ; TIME = D0 SECOND
 CMP.B #0,D0
 BEQ FIN
 MOVE.L #$FFD00000,D2
LOOP
 ADD.L #$01,D2
 BNE LOOP
 ADD.L #$FFFFFFFF,D0
 BNE DELAY
FIN
